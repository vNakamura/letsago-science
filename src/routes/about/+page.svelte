	<h2 class="text-xl">About</h2>
	<p>
		This list is maintained by fans and updated as Dr. Jeff Gerstmann, Man of Science, continues his
		research into the Nintendo 64 library. The goal is to rank all Nintendo 64 games released in
		North America based on their scientific merit, as determined by Gerstmann's rigorous testing and
		analysis.
	</p>
	<p>
		You can watch the ongoing investigation live on <a href="https://www.twitch.tv/jeffgerstmann"
			><span class="icon-[fa7-brands--twitch]"></span>twitch.tv/jeffgerstmann</a
		>
		or view the study archives on
		<a href="https://www.youtube.com/show/VLPLAXQPbxc1JcU?sbp=Kgs4YjlWM21UVlJKRUAB"
			><span class="icon-[fa7-brands--youtube]"></span>youtube.com/TheJeffGerstmannShow</a
		>.
	</p>
	<p>
		Prior to the Nintendo 64, Dr. Gerstmann's research focused on the Nintendo Entertainment System.
		You can view the complete ranking at <a href="http://8bitnintendo.science"
			><span class="icon-[teenyicons--nes-outline]"></span>8bitnintendo.science</a
		>.
	</p>
